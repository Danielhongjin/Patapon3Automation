package types;

/*
 * Represents the different generic inputs.
 * @author Daniel Jin
 * @version 1.0
 */
public enum Input {
    // DPad inputs
    UP,
    DOWN,
    LEFT,
    RIGHT,
    // Shape inputs
    CIRCLE,
    CROSS,
    SQUARE,
    TRIANGLE,
    // Trigger inputs
    L,
    R,
    // Analog inputs
    AUP,
    ADOWN,
    ALEFT,
    ARIGHT,
    // Menu inputs
    START,
    SELECT
}